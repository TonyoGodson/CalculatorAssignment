package com.example.classwork

class Testingcalculator {

    fun testSum(num1:Int, num2:Int):Int{
        return num1 + num2
    }
    fun testSub(num1:Int, num2:Int):Int{
        return (num2) - (num1)
    }
    fun testMul(num1:Int, num2:Int):Int{
        return num1 * num2
    }
    fun testDiv(num1:Float, num2:Float):Float{
        return num1 / num2
    }
}